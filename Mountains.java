import java.awt.*;
import java.applet.*;
public class Mountains extends Applet{

    public void paint(Graphics g){


        //background
        g.drawground(g);
        g.drawSky(g);


        //mountains
        g.drawPeaks(g);
        g.drawsnow(g);
        g.drawFlag(g);
        g.drawSun(g);


        //fields
        g.drawTree(g);
        g.drawBushes(g);
        g.drawHouse(g);
        g.drawLake(g);






















    }
}
